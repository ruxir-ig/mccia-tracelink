/**
 * Translate an English string into the target language.
 *
 * Strategy:
 *   1. Exact phrase match in dictionary → return translation
 *   2. No match → return original English (safe fallback)
 *
 * This keeps the engine simple, predictable, and easy to debug.
 * Complex sentences should be added to DICT as full phrases.
 */
export function translate(text: string, lang: TLang): string {
  if (lang === "en") return EN_ALIASES[text] ?? text;

  // Try exact match first
  const entry = DICT[text];
  if (entry) {
    return entry[lang] ?? text;
  }

  // No exact match — return English as safe fallback.
  // (In a future version we could add word-by-word composition
  //  here, but exact-match keeps quality high.)
  return text;
}

/**
 * Interpolate {vars} into a translated string.
 */
export function t(text: string, lang: TLang, vars?: Record<string, string | number>): string {
  let result = translate(text, lang);
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      result = result.replace(`{${k}}`, String(v));
    });
  }
  return result;
}
